package main

import "fmt"

func main() {
	var angka1 uint16 = 256
	var angka2 int = 123
	var decimal float32 = 2.54
	var bolean bool = false

	fmt.Printf("angka1 = %d \n", angka1)
	fmt.Printf("angka2 = %d \n", angka2)
	fmt.Printf("decimal = %f \n", decimal)
	fmt.Printf("bolean = %t \n", bolean)

}
